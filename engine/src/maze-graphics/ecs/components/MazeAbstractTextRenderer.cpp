    //////////////////////////////////////////
    AbstractTextRenderer::AbstractTextRenderer()
    {
        
    }

    //////////////////////////////////////////
    AbstractTextRenderer::~AbstractTextRenderer()
    {
        
    }

    //////////////////////////////////////////
    bool AbstractTextRenderer::init(RenderSystem* _renderSystem)
    {
        if (!_renderSystem)
            return false;

        m_renderSystem = _renderSystem;

        return true;
    }

    //////////////////////////////////////////
    bool AbstractTextRenderer::init(
        Component* _component,
        EcsWorld* _world,
        EntityCopyData _copyData)
    {
        m_renderSystem = _component->castRaw<AbstractTextRenderer>()->m_renderSystem;

        if (!Component::init(
            _component,
            _world,
            _copyData))
            return false;

        return true;
    }

